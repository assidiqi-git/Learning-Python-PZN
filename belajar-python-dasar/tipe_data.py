# Integer (int) - Bilangan Bulat
umur = 99
print(type(umur))

# Float - Bilangan Desimal
tinggi = 171.3
print(type(tinggi))

# String (str) - Teks
# single line
nama = "Assidiqi"
kota = 'Banjarmasin'
# multi line
alamat = """
Kota Banjarmasin
Provinsi Kalimantan Selatan
Indonesia
"""
print(type(kota))
print(type(alamat))

# Boolean (bool) - True/False
is_working = True
print(type(is_working))
